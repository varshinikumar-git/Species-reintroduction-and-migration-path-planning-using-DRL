class DataLoader:
    def __init__(self, grid_size=100):
        self.grid_size = grid_size
        self.layers = {}

    def create_synthetic(self, pattern='gradient'):
        x = np.linspace(0, 1, self.grid_size)
        y = np.linspace(0, 1, self.grid_size)
        xx, yy = np.meshgrid(x, y)

        if pattern == 'gradient':
            base = (xx * 0.6 + yy * 0.4)
        elif pattern == 'center':
            base = 1 - np.sqrt((xx - 0.5)**2 + (yy - 0.5)**2)
        elif pattern == 'patches':
            base = np.zeros((self.grid_size, self.grid_size))
            # ✅ Create realistic habitat patches
            for _ in range(12):
                cx, cy = np.random.rand(2)
                radius = np.random.uniform(0.12, 0.20)
                patch = np.exp(-((xx - cx)**2 + (yy - cy)**2) / (2 * radius**2))
                base += patch
            base = base / (base.max() + 1e-8)
        elif pattern == 'diverse':
            base = np.random.rand(self.grid_size, self.grid_size)
            base = gaussian_filter(base, sigma=6)
        else:
            base = (xx + yy) / 2

        noise = np.random.rand(self.grid_size, self.grid_size) * 0.25
        smooth = gaussian_filter(base + noise, sigma=3)
        return np.clip(smooth, 0, 1)

    def load_or_create(self, filename, pattern='gradient'):
        path = f'data/drl_ready/{filename}'
        if os.path.exists(path):
            try:
                if filename.endswith('.npy'):
                    data = np.load(path)
                else:
                    img = Image.open(path).convert('RGB')
                    img = img.resize((self.grid_size, self.grid_size))
                    data = np.array(img).mean(axis=2) / 255.0
                if data.shape != (self.grid_size, self.grid_size):
                    zoom_factor = self.grid_size / data.shape[0]
                    data = zoom(data, zoom_factor, order=1)
                print(f"  ✓ {filename}")
                return data
            except:
                pass
        print(f"  → Synthetic {filename}")
        return self.create_synthetic(pattern)

    def load_species_data(self, species_name):
        for fname in [f'{species_name}_occurrences.csv', f'{species_name}_occurrences_minimal.csv']:
            path = f'data/species/{fname}'
            if os.path.exists(path):
                try:
                    df = pd.read_csv(path)
                    print(f"  ✓ {fname}: {len(df)} points")
                    return df
                except:
                    pass
        print(f"  → Synthetic {species_name}")
        n = np.random.randint(180, 280)
        return pd.DataFrame({
            'latitude': np.random.uniform(-25, -12, n),
            'longitude': np.random.uniform(43, 50, n)
        })

    def occurrences_to_grid(self, df):
        grid = np.zeros((self.grid_size, self.grid_size))
        if len(df) == 0:
            return grid
        lats = ((df['latitude'].values + 25) / 13 * (self.grid_size - 1)).astype(int)
        lons = ((df['longitude'].values - 43) / 7 * (self.grid_size - 1)).astype(int)
        lats = np.clip(lats, 0, self.grid_size - 1)
        lons = np.clip(lons, 0, self.grid_size - 1)
        for i, j in zip(lats, lons):
            grid[i, j] += 1
        grid = gaussian_filter(grid, sigma=4)
        return grid / (grid.max() + 1e-8)

